// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(a,b) {
  //find the lower
  if(a<b){
    return a;
  } else {
    return b;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(a) {
  //use remainder to see if even
  if  (a % 2===0){
    return true;
  } else{
    return false;
  }

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string,letter) {
  let counter = 0;
  for(let i=0; i<string.length;i++){
    //matches the case of letters before comparing
    if (string[i].toUpperCase() === letter.toUpperCase){
      counter++;
      //catches space and other not letter characters
    } else if(string[i] === letter){
      counter ++;
    }
  }
  return counter;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let count= 0
  for(let i = 0; i<string.length; i++){
    if (string[i]==="B"){
      count++;
    }
    
  }
  return count;

}

